cache = {}

def algo(arg1: str, arg2: str) -> int:
    res = cache.get((arg1, arg2))
    if res is not None:
        return res
    arg1_first = arg1[0]
    arg2_first = arg2[0]

    #print(f'{arg1=}')
    #print(f'{arg2=}')
    #print('')

    if arg1_first == '\x00' and arg2_first == '\x00':
        res = 0
        cache[(arg1, arg2)] = res
        return res

    if arg1_first == '\x00':
        res = algo(arg1, arg2[1:]) + 1
        cache[(arg1, arg2)] = res
        return res

    if arg2_first == '\x00':
        res = algo(arg1[1:], arg2) + 1
        cache[(arg1, arg2)] = res
        return res

    if arg1_first == arg2_first:
        res = algo(arg1[1:], arg2[1:])
        cache[(arg1, arg2)] = res
        return res

    md1 = algo(arg1[1:], arg2) + 1
    md2 = algo(arg1, arg2[1:]) + 1
    md3 = algo(arg1[1:], arg2[1:]) + 1

    res = min(md1, md2, md3)
    cache[(arg1, arg2)] = res
    return res


